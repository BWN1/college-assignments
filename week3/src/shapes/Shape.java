package shapes;

/** This class creates a Shape interface */
public interface Shape {
	
	/**
	 * Calculate the perimeter of the shape
	 * @return double Returns the perimeter of the shape
	 * */
	public double calcPerimeter();
	
	/**
	 * Return a string representing the shape
	 * @return string This returns a string representing the shape 
	 * */
	public String toString();
}
